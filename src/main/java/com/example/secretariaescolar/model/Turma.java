package com.example.secretariaescolar.model;

public class Turma {

    private int id_turma;
    private String nome;

    public Turma() {

    }

    public int getId_turma() {
        return id_turma;
    }

    public void setId_turma(int id_turma) {
        this.id_turma = id_turma;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}